<?php

namespace App\Contracts;

use Exception;

/**
 * Интерфейс для экспорта отчетов в различные форматы.
 *
 * Определяет контракт для классов, которые экспортируют данные отчета в файлы
 * определенного формата (например, Word, Excel).
 */
interface ReportExporterInterface
{
    /**
     * Экспортирует данные отчета в файл.
     *
     * Принимает массив данных отчета и возвращает путь к сгенерированному файлу.
     *
     * @param array $reportData Данные отчета для экспорта
     * @return string Путь к временному файлу с отчетом
     * @throws Exception Если экспорт не удался (например, ошибка валидации данных или записи файла)
     */
    public function export(array $reportData): string;
}
